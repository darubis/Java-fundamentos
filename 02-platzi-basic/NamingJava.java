            // Upper Camel Case solo para clases
            public class NamingJava {
                public static void main(String[] args) {
                    int cellphone = 33337777;
                    int cellPhone = 55553333;
                    System.out.println(cellphone);
                    System.out.println(cellPhone);
            
                    // es valido empezar una variable con signo $
                    String $countryName = "Spain";
            
                    // es valido empezar una variable con _ guion bajo
                    String _backgroundColor = "Green";
            
                    // es valido poner signo $ al final
                    String currency$ = "MXN";
            
                    String background_color = "Blue";
                    
                    int POSITION = -5;
                    int MAX_WIDTH = 999;
                    int MIN_WIDTH = 1;
            
                    // Lower Camel Case para variables y metodos
                    String fullName = "Irene Anahi Salgado Diaz de la vega";
                    int sizeINCentimeters = 26;
                }
            }
