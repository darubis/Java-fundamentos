public class Operaciones {
    public static void main(String[] args){
        int numero_uno = 4;
        int numero_dos = 4;
        int resultado = 0;

        resultado = numero_uno + numero_dos / 2; // 6
        System.out.println(resultado);

        resultado = (numero_uno + numero_dos) / 2; // 4
        System.out.println(resultado);
    }
}
